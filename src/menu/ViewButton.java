package menu;

public class ViewButton extends Button {
	private String type;
	private String url;
	
	public String getType() {
		return type;
	}
	
	public void setType(String value) {
		type = value;
	}
	
	public String getUrl() {
		return url;
	}
	
	public void setUrl(String value) {
		url = value;
	}
}
