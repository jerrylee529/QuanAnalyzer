package menu;

public class ClickButton extends Button {

	private String key;
	private String type;
	
	public String getKey() {
		return key;
	}
	
	public void setKey(String value) {
		key = value;
	}
	
	public String getType() {
		return type;
	}
	
	public void setType(String value) {
		type = value;
	}
}
