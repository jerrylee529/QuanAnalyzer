
public class ReportInfo {
	private String code;
	
	private String name;
	
	private double roe;
	
	private double pb;
	
	private double pe;
	
	private double peg;
	
	public String getCode() {
		return code;
	}
	
	public void setCode(String value) {
		code = value;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String value) {
		name = value;
	}
	
	public double getROE() {
		return roe;
	}
	
	public void setROE(double value) {
		roe = value;
	}

	public double getPB() {
		return pb;
	}
	
	public void setPB(double value) {
		pb = value;
	}

	public double getPE() {
		return pe;
	}
	
	public void setPE(double value) {
		pe = value;
	}

	public double getPEG() {
		return peg;
	}
	
	public void setPEG(double value) {
		peg = value;
	}
}
