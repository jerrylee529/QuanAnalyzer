
public class TradeInfo {
	private String period; // 周期
	private String roi;  // 收益率
	private String buy_date; // 买入日期
	private String sell_date;// 卖出日期

	private String buy_price; // 买入价格
	private String sell_price; // 卖出价格
	
	public String getPeriod() {
		return period;
	}
	
	public void setPeriod(String value) {
		period = value;
	}
	
	public String getRoi() {
		return roi;
	}
	
	public void setRoi(String value) {
		roi = value;
	}
	
	public String getBuy_date() {
		return buy_date;
	}
	
	public void setBuy_date(String value) {
		buy_date = value;
	}
	
	public String getSell_date() {
		return sell_date;
	}
	
	public void setSell_date(String value) {
		sell_date = value;
	}
	
	public String getBuy_price() {
		return buy_price;
	}
	
	public void setBuy_price(String value) {
		buy_price = value;
	}
	
	public String getSell_price() {
		return sell_price;
	}
	
	public void setSell_price(String value) {
		sell_price = value;
	}
}
